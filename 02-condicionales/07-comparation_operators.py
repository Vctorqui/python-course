
# Igualdad ==
print(5 == 5) # True
print(5 == 3) # False
print('hello' == 'hello') # True


# Operador de desigualdad !=
print(5 != 5) #  False
print(5 != 3) # True
print('hello' != 'hello') # False


# Operador mayor que >
print(5 > 4) # True
print(2 > 4) # False

# Operador menor que <
print(5 < 4) # False
print(2 < 4) # True

