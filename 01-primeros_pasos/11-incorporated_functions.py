
# Funciones incorporadas - Funciones nativas (built-in functions)

# Ejemplos de funciones incorporadas en Python

# Función print() - Imprime en la consola
print("Hola, Mundo!")  # Salida: Hola, Mundo!
# Función type() - Devuelve el tipo de dato de una variable
print(type(42))        # Salida: <class 'int'>
# Función str() - Convierte un valor a string
print(str(42))        # Salida: '42'

# Métodos - Tareas específicas
# Los métodos son funciones asociadas a los objetos (tipos de datos)
message = "Es un buen programador"
message.upper() # Convierte a mayúsculas -> 'ES UN BUEN PROGRAMADOR'

new_message = message.replace("buen", "excelente") # Reemplaza una subcadena por otra -> 'Es un excelente programador'
print(new_message)