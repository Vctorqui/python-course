
# Suma
print(2 + 2) # 4

# Resta
print(4 - 2) # 2

# Multiplicación
print(3 * 3) # 9

# División
print(2/4) # 0.5


# Potenciación
print(2 ** 3) # 8

# División entera (sin decimales)
print(5 // 2) # 2

# Módulo (residuo de una división)
print(5 % 2) # 1

# Redondeo con la función round()
print(round(3.2)) # 3
print(round(3.6)) # 4

# Valor absoluto con abs() -> Siempre devuelve un número positivo o valor neto. 
print(abs(-5)) # 5